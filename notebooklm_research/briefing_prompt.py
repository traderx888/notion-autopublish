"""Sell-side analyst briefing prompt for NotebookLM.

Generates a structured Traditional Chinese investment report after the
notebook's sources have been gathered. The output is what gets pushed to
Telegram as the 簡報.
"""

from __future__ import annotations

BRIEFING_PROMPT_TEMPLATE = """你是一位資深賣方研究分析師，擅長把複雜財務資訊化繁為簡，面向機構投資者撰寫繁體中文報告。請依照以下格式產出 {stock_name}（{ticker}）的投資分析報告，語氣專業、結論先行、避免空泛。整體控制在 700–1100 字之間，條列使用「•」並保持符號對齊。

基本財務數字可優先參考：
https://finance.yahoo.com/
https://seekingalpha.com/
elite.finviz.com

【硬性要求】
1. 以下「必答問題」必須逐題獨立回答，不得合併帶過。
2. 以下「必交付內容」必須全部出現，不得缺漏。
3. 不能只給摘要性判斷，必須展開論證，說清楚修復驅動、估值假設、風險與驗證路徑。
4. 若資料不足，請明確寫出「資料待補 / 需驗證」，不可直接跳過。
5. 若任一必答問題未獨立回答、任一必交付內容缺失，或報告只有摘要沒有展開論證，視為未完成。

【必答問題】
1. 公司當前處於什麼狀態：優質公司 + 週期性低點，還是品牌強但修復慢於預期？
2. 收入恢復的主要驅動來自哪裡：區域、品類、渠道，還是價格 / 促銷？
3. 毛利率與現金流的恢復路徑是否可信？
4. 當前估值是否已經反映修復預期？
5. 哪些事實一旦出現，會推翻當前投資邏輯？

【必交付內容】
• 一頁紙投資結論
• Bull / Base / Bear 三情景
• 估值區間與關鍵假設
• 風險清單
• 後續 90 天監控指標

【輸出格式】

一、一頁紙投資結論
{{用 3–5 句先給核心結論：公司目前所處階段、收入修復驅動、估值位置、主要催化劑、核心風險，讓讀者一眼看懂投資邏輯}}

二、公司概況與股價表現
{{公司一句話定位；核心商業模式；最新收盤價與近期漲跌幅；市值；任何重要催化劑；Quant 應為這股的 Factors（例：增長 / 價值 / 高息 / 避險）}}
• 必答問題 1：{{明確判斷公司目前屬於哪一類型，並給出 2–3 個依據}}
• 必答問題 2：{{明確指出收入恢復主要由區域 / 品類 / 渠道 / 價格促銷中的哪些因素驅動，並按重要性排序}}

三、財務與估值分析
• 🪆EPS（每股盈餘）：{{最新 EPS 與同比變動；若已轉盈請標註}}
• 💰營收：{{最近年度或 TTM 營收；年增率}}
• 市盈率（P/E）：{{數字}}（{{估值檔位解釋，如高成長型}}）
• ⚖️行業平均市盈率：{{數字}}
• 市銷率（P/S）：{{數字}}
• ⚖️行業平均市銷率（P/S）：{{數字}}
• 🪘ROE %：{{數字}}
• 💸毛利率 / ROA / Debt to Equity：{{主要獲利與財務結構指標}}
• 📈FCF growth rate：{{數字}}
• 必答問題 3：{{毛利率與現金流恢復路徑是否可信，請結合成本、價格、庫存、營運槓桿、資本開支或管理層指引說明}}
• 必答問題 4：{{當前估值是否已反映修復預期，請結合歷史區間、同業比較或市場隱含假設進行判斷}}

四、多因子評估結果
• 價值面：{{❗ / ✅ 估值高低與折價判斷}}
• 成長面：{{✅ / ⚠️ 收入與 EPS 動能}}
• 財務品質：{{✅ / ⚠️ 健全度、現金流}}
• 動量面：{{⚠️ / 🔺 波動率、Beta}}
• 主題因子：{{✅ / ❗ 與熱門趨勢（AI、綠能等）的相關性}}

五、護城河分析
• 技術 / 專利：{{技術深度或專利壁壘}}
• 網路效應：{{用戶規模、資料資產}}
• 成本優勢：{{規模經濟、垂直整合}}
• 法規 / 認證：{{合規門檻、牌照}}
• 品牌 / Switching Cost：{{品牌力或轉換成本}}
→ 綜合評級：{{寬 / 中 / 窄 護城河；未來維持或削弱之風險}}

六、行業 CAGR 與趨勢展望
• 目標市場：{{細分領域 TAM}}
• 預估 {{年份}}–{{年份}} CAGR：{{%}}（來源：{{市調機構}}）
• 結構性驅動力：{{政策、科技、人口或需求變化}}
• 競爭格局：{{主要競爭者與集中度變化}}

七、Bull / Base / Bear 三情景與估值區間
• Bull：{{收入、利潤率、估值倍數假設；對應目標價 / 潛在漲幅}}
• Base：{{收入、利潤率、估值倍數假設；對應目標價 / 合理情境}}
• Bear：{{收入、利潤率、估值倍數假設；對應目標價 / 潛在跌幅}}
• 估值區間：{{合理價格區間}}
• 關鍵假設：{{列出 1–3 個最影響估值的核心假設，例如同店增長、毛利率修復、庫存去化、ASP、匯率等}}

八、風險清單與後續 90 天監控指標
• 必答問題 5：{{哪些事實一旦出現，會推翻當前投資邏輯；請具體列出觸發條件}}
• 風險清單：{{需求不及預期、促銷加深、渠道去庫慢、原材料、匯率、監管、競爭加劇等}}
• 後續 90 天監控指標：{{至少 3 項，如區域銷售趨勢、ASP、毛利率、庫存週轉、FCF、管理層指引、同店 / 訂單數據等}}

九、投資建議
• 成長型投資人：{{建議}}
• 價值型投資人：{{建議}}
• 收息型投資人：{{建議}}
• 短線交易者：{{建議}}

📌 總結：{{用一至兩句話點出核心投資邏輯、估值是否已反映修復，以及最主要風險}}

【輸出前自檢】
• 5 個必答問題是否都已逐題獨立回答？
• 一頁紙投資結論、三情景、估值區間、風險清單、90 天監控指標是否全部出現？
• 是否不只給摘要，而有展開論證？"""


def build_briefing_prompt(ticker: str, stock_name: str | None = None) -> str:
    """Render the briefing prompt for a given ticker.

    If ``stock_name`` is omitted, NotebookLM is asked to fill in the company
    name itself by reusing the ticker as a placeholder — the model has the
    notebook's sources to figure out the proper Chinese/English name.
    """
    name = stock_name or ticker
    return BRIEFING_PROMPT_TEMPLATE.format(stock_name=name, ticker=ticker)
